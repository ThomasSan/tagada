#include <unistd.h>
void	ft_putchar(char c)
{
	write(1, &c, 1);
}


int		main(int ac, char **av)
{
	int i;
	int j;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			if ((av[1][i] == ' ' || av[1][i] == '\t' || av[1][i] == '\n' || av[1][i] == '\r' || av[1][i] == '\f'
						|| av[1][i] == '\v') && (av[1][i + 1] > 32 && av[1][i + 1] <= 126))
				j = i + 1;
			i++;
		}
		while (av[1][j] >= 33 && av[1][j] <= 126)
		{
			ft_putchar(av[1][j]);
			j++;
		}
	}
	ft_putchar('\n');
	return (0);
}
