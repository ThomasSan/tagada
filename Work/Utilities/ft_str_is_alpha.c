int		ft_str_is_alpha(char *str)
{
	int i;
	int num;

	i = 0;
	num = 0;
	while (str[i] != '\0')
	{
		if ((str[i] > 64 && str[i] < 91) || (str[i] > 96 && str[i] < 123))
			num++;
		i++;
	}
	if (num == i)
		return (1);
	else
		return (0);
}
